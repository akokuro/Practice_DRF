def gen(repeatParts = False,
                 separator = '',
                 lists = (adjective, noun)
                 ):
    name = []
    for word in lists:
        part = None
        while not part or (part in name and not repeatParts):
            part = random.choice(word)
        name.append(part)
    return separator.join(name)